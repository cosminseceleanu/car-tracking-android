package com.cosmin.cartracking.model;


public class Page {
    private int size;
    private int totalElements;
    private int totalPages;
    private int number;

    public int getSize() {
        return size;
    }

    public int getTotalElements() {
        return totalElements;
    }

    public int getTotalPages() {
        return totalPages;
    }

    public int getNumber() {
        return number;
    }
}
